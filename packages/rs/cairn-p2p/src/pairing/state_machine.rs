    const TEST_PASSWORD: &[u8] = b"test-pairing-password-42";

    // --- Initiation flow tests ---

    #[test]
    fn initiation_flow_initiator_creates_request() {
        let identity = LocalIdentity::generate();
        let (session, msg) =
            PairingSession::new_initiator(identity, TEST_PASSWORD, DEFAULT_PAIRING_TIMEOUT);

        assert!(matches!(
            session.state(),
            PairingState::AwaitingPakeExchange
        ));
        assert_eq!(session.role(), PairingRole::Initiator);
        assert_eq!(session.flow_type(), PairingFlowType::Initiation);

        match msg {
            PairingMessage::Request(req) => {
                assert_eq!(req.flow_type, PairingFlowType::Initiation);
                assert!(req.pake_msg.is_some());
                assert_eq!(req.nonce.len(), 16);
            }
            _ => panic!("expected PairRequest"),
        }
    }

    #[test]
    fn initiation_flow_full_exchange() {
        let alice_id = LocalIdentity::generate();
        let bob_id = LocalIdentity::generate();

        // Alice creates an initiator session with a password
        let (mut alice, alice_req_msg) =
            PairingSession::new_initiator(alice_id, TEST_PASSWORD, DEFAULT_PAIRING_TIMEOUT);
        assert!(matches!(alice.state(), PairingState::AwaitingPakeExchange));

        // Bob creates a responder session with the same password
        let mut bob = PairingSession::new_responder(bob_id, TEST_PASSWORD, DEFAULT_PAIRING_TIMEOUT);
        assert!(matches!(bob.state(), PairingState::Idle));

        // Bob handles Alice's PairRequest, gets a PairChallenge back
        let bob_reply = bob.handle_message(alice_req_msg).unwrap();
        assert!(bob_reply.is_some());
        assert!(matches!(bob.state(), PairingState::AwaitingVerification));
        let challenge = bob_reply.unwrap();
        assert!(matches!(challenge, PairingMessage::Challenge(_)));

        // Alice handles Bob's PairChallenge, gets a PairResponse (key confirmation)
        let alice_reply = alice.handle_message(challenge).unwrap();
        assert!(alice_reply.is_some());
        assert!(matches!(alice.state(), PairingState::AwaitingConfirmation));

        // Bob handles Alice's PairResponse, verifies key confirmation, sends PairConfirm
        let bob_confirm = bob.handle_message(alice_reply.unwrap()).unwrap();
        assert!(bob_confirm.is_some());
        assert!(matches!(bob.state(), PairingState::AwaitingConfirmation));

        // Alice handles Bob's PairConfirm, completes
        let alice_final = alice.handle_message(bob_confirm.unwrap()).unwrap();
        assert!(matches!(alice.state(), PairingState::Completed));
        assert!(alice.shared_key().is_some());

        // If Alice sent a confirm back, Bob processes it
        if let Some(confirm_back) = alice_final {
            let _ = bob.handle_message(confirm_back).unwrap();
            assert!(matches!(bob.state(), PairingState::Completed));
            assert!(bob.shared_key().is_some());
        }

        // Both sides derived the same shared key
        assert_eq!(alice.shared_key().unwrap(), bob.shared_key().unwrap());
    }

    #[test]
    fn spake2_wrong_password_produces_different_keys() {
        let (alice_state, alice_msg) = Spake2::<Ed25519Group>::start_a(
            &Password::new(b"correct-password"),
            &Identity::new(SPAKE2_ID_INITIATOR),
            &Identity::new(SPAKE2_ID_RESPONDER),
        );

        let (bob_state, bob_msg) = Spake2::<Ed25519Group>::start_b(
            &Password::new(b"wrong-password"),
            &Identity::new(SPAKE2_ID_INITIATOR),
            &Identity::new(SPAKE2_ID_RESPONDER),
        );

        let alice_key = alice_state.finish(&bob_msg).unwrap();
        let bob_key = bob_state.finish(&alice_msg).unwrap();

        // With mismatched passwords, the derived keys won't match.
        assert_ne!(alice_key, bob_key);
    }


    // --- Full standard flow with key confirmation ---

    #[test]
    fn standard_flow_key_confirmation_exchange() {
        let alice_id = LocalIdentity::generate();
        let bob_id = LocalIdentity::generate();

        let (mut alice, alice_req_msg) =
            PairingSession::new_standard_initiator(alice_id, DEFAULT_PAIRING_TIMEOUT);

        let mut bob = PairingSession::new_standard_responder(bob_id, DEFAULT_PAIRING_TIMEOUT);

        // Bob handles Alice's request
        let _bob_reply = bob.handle_message(alice_req_msg).unwrap();

        // Both have the same shared key (simulated — in real flow, Noise XX provides this)
        let shared = vec![0xAB; 32];
        alice.set_shared_key(shared.clone());
        bob.set_shared_key(shared);

        // Set nonces so both sides use the same salt for key confirmation.
        bob.remote_nonce = Some(alice.local_nonce.clone());
        alice.remote_nonce = Some(bob.local_nonce.clone());

        // Alice sends key confirmation (PairResponse) and advances to AwaitingConfirmation.
        let alice_response = alice.send_key_confirmation().unwrap();
        assert!(matches!(alice.state(), PairingState::AwaitingConfirmation));

        // Bob receives Alice's PairResponse, verifies, sends PairConfirm.
        let bob_reply = bob.handle_message(alice_response).unwrap();
        assert!(bob_reply.is_some());
        assert!(matches!(bob.state(), PairingState::AwaitingConfirmation));

        // Alice receives Bob's PairConfirm, verifies, completes.
        let alice_reply = alice.handle_message(bob_reply.unwrap()).unwrap();
        assert!(matches!(alice.state(), PairingState::Completed));
        assert!(alice.shared_key().is_some());

        // If Alice sent a confirm back, Bob processes it to complete.
        if let Some(alice_confirm_back) = alice_reply {
            let _ = bob.handle_message(alice_confirm_back).unwrap();
            assert!(matches!(bob.state(), PairingState::Completed));
            assert!(bob.shared_key().is_some());
        }
    }

    #[test]
    fn wrong_key_confirmation_fails() {
        let alice_id = LocalIdentity::generate();
        let bob_id = LocalIdentity::generate();

        let (mut alice, alice_req_msg) =
            PairingSession::new_standard_initiator(alice_id, DEFAULT_PAIRING_TIMEOUT);

        let mut bob = PairingSession::new_standard_responder(bob_id, DEFAULT_PAIRING_TIMEOUT);

        bob.handle_message(alice_req_msg).unwrap();

        // Set different shared keys to simulate MITM
        alice.set_shared_key(vec![0xAA; 32]);
        bob.set_shared_key(vec![0xBB; 32]);

        bob.remote_nonce = Some(alice.local_nonce.clone());
        alice.remote_nonce = Some(bob.local_nonce.clone());

        // Alice sends her key confirmation (derived from her key)
        let alice_confirm = alice.compute_key_confirmation(b"initiator").unwrap();

        // Bob tries to verify — should fail because keys differ
        let result = bob.handle_message(PairingMessage::Response(PairResponse {
            key_confirmation: alice_confirm,
        }));

        assert!(result.is_err());
        assert!(matches!(result.unwrap_err(), PairingError::PakeFailure));
        assert!(matches!(bob.state(), PairingState::Failed(_)));
    }

